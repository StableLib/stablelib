class TestRandomSource {
    isAvailable = true;
    key = new Uint32Array([3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5, 8, 9, 7, 9, 3, 2, 3, 8, 4, 6, 2, 6, 4, 3, 3, 8, 3, 2, 7, 9, 5]);
    nonce = new Uint32Array(8);
    out = new Uint32Array(64);
    outleft = 0;

    randomBytes(len: number): Uint8Array {
        const dst = new Uint8Array(len);
        let pos = 0;
        while (len > 0) {
            if (this.outleft === 0) {
                for (let i = 0; i < 4; i++) {
                    if ((this.nonce[i] = (this.nonce[i] + 1) | 0) !== 0) {
                        break;
                    }
                }
                stream(this.key, this.nonce, this.out);
                this.outleft = 64;
            }
            dst[pos++] = this.out[--this.outleft];
            len--;
        }
        return dst;
    }
}


